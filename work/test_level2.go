// 计算用户积分等级
func CalcPointLevel(Point int32) int8 {
	if Point < 0 {
		return 1
	}
	var level int8 = 1
	for pointCfg, levelCfg := range pointLevelMap {
		if pointCfg > Point {
			return level
		}
		level = levelCfg
	}
	return level
}
